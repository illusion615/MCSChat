/**
 * Icon Category Mappings
 * Helper object to map icon names to their categories
 */
export const IconCategories = {
    core: Object.keys(CoreIcons),
    media: Object.keys(MediaIcons),
    navigation: Object.keys(NavigationIcons),
    users: Object.keys(UserIcons),
    avatars: Object.keys(AvatarIcons),
    content: Object.keys(ContentIcons),
    ui: Object.keys(UIIcons)
};

/**
 * Get icon category by name
 * @param {string} iconName - Name of the icon
 * @returns {string|null} Category name or null if not found
 */
export function getIconCategory(iconName) {
    for (const [category, icons] of Object.entries(IconCategories)) {
        if (icons.includes(iconName)) {
            return category;
        }
    }
    return null;
}

/**
 * Get all icons in a specific category
 * @param {string} category - Category name
 * @returns {Object} Object containing icons in the category
 */
export function getIconsByCategory(category) {
    switch (category.toLowerCase()) {
        case 'core': return CoreIcons;
        case 'media': return MediaIcons;
        case 'navigation': return NavigationIcons;
        case 'users': return UserIcons;
        case 'avatars': return AvatarIcons;
        case 'content': return ContentIcons;
        case 'ui': return UIIcons;
        default: return {};
    }
}

/**
 * Search icons by name or category
 * @param {string} query - Search query
 * @returns {Array} Array of matching icon names
 */
export function searchIcons(query) {
    const lowercaseQuery = query.toLowerCase();
    const results = [];
    
    Object.keys(SVGIcons).forEach(iconName => {
        if (iconName.toLowerCase().includes(lowercaseQuery)) {
            results.push(iconName);
        }
    });
    
    return results;
}
